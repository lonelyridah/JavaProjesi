    private void ekle_buttonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ekle_buttonMouseClicked
        DataBase db = new DataBase();
       
        String veri_tipi = null ;
        String kitabin_turu = null ;
        ArrayList<String> kitap_nesnesi = new ArrayList<>() ;
        
        String kitabin_numarasi_string = kitabin_numarasi_textfield.getText();
        kitap_nesnesi.add(kitabin_numarasi_string);
        
        String kitabin_adi = kitabin_adi_textfield.getText();
        kitap_nesnesi.add(kitabin_adi);
        
        String yazarin_adi = yazarin_adi_textfield.getText();
        kitap_nesnesi.add(yazarin_adi);
        
        String yayin_evi = yayin_evi_textfield.getText();
        kitap_nesnesi.add(yayin_evi);
        
        String sayfa_sayisi_string = sayfa_numarasi_textfield.getText();
        kitap_nesnesi.add(sayfa_sayisi_string);
         
        String basim_yili_string = basim_yili_textfield.getText();
        kitap_nesnesi.add(basim_yili_string); 
        
        String ISBN_num = ISBN_num_textfield.getText();
        kitap_nesnesi.add(ISBN_num);
        
        String fiyat_string = fiyat_textfield.getText();
        kitap_nesnesi.add(fiyat_string);
   
        int kitabin_turu_num = kitabin_turu_combo_box.getSelectedIndex();
        
        switch (kitabin_turu_num) {
            case 1:
                veri_tipi = "kitap_yayinciligi.Klasik";
                kitabin_turu = "Klasikler";
                break;
            case 2:
                veri_tipi = "kitap_yayinciligi.Bilgisayar";
                kitabin_turu = "Bilgisayar";
                break;
            case 3:
                veri_tipi = "kitap_yayinciligi.Felsefe";
                kitabin_turu = "Felsefe";
                break;
            case 4:
                veri_tipi = "kitap_yayinciligi.Dini";
                kitabin_turu = "Dini";
                break;
            case 5:
                veri_tipi = "kitap_yayinciligi.Tarih";
                kitabin_turu = "Tarih";
                break;
            case 6:
                veri_tipi = "kitap_yayinciligi.Egitim";
                kitabin_turu = "Egitim";
                break;
            default:
                break;
        }
        
        kitap_nesnesi.add(kitabin_turu);
        db.veri_ekleme(veri_tipi, kitap_nesnesi);
        db.eklenen_kitap_sayisi_degistir(veri_tipi,1);
        eklendi_textfield.setText("Yeni kitap basariyla eklendi..");
    }//GEN-LAST:event_ekle_buttonMouseClicked

