    private void back_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_back_buttonActionPerformed
        if ( back_combo_box.getSelectedIndex() == 1 )
        {
            Klasik_Arayuz screen = new Klasik_Arayuz();
            screen.setVisible(true);
            this.setVisible(false);
        }
        else if ( back_combo_box.getSelectedIndex() == 2 )
        {
            Bilgisayar_Arayuz screen = new Bilgisayar_Arayuz();
            screen.setVisible(true);
            this.setVisible(false);
        }
        else if ( back_combo_box.getSelectedIndex() == 3 )
        {
            Felsefe_Arayuz screen = new Felsefe_Arayuz();
            screen.setVisible(true);
            this.setVisible(false);
        }
        else if ( back_combo_box.getSelectedIndex() == 4 )
        {
            Dini_Arayuz screen = new Dini_Arayuz();
            screen.setVisible(true);
            this.setVisible(false);
        }
        else if ( back_combo_box.getSelectedIndex() == 5 )
        {
            Tarih_Arayuz screen = new Tarih_Arayuz();
            screen.setVisible(true);
            this.setVisible(false);
        }
        else if ( back_combo_box.getSelectedIndex() == 6 )
        {
            Egitim_Arayuz screen = new Egitim_Arayuz();
            screen.setVisible(true);
            this.setVisible(false);
        }
    }//GEN-LAST:event_back_buttonActionPerformed

